function fn_settings_variants_addons_cp_fast_auth_order_statuses()
{
    $order_statuses = fn_get_statuses(STATUSES_ORDER, array(), true);

    $addon_settings = array();
    
    if (!empty($order_statuses)) {

        foreach ($order_statuses as $status_data) {
            $addon_settings[$status_data['status']] = $status_data['description'];
        }

    }
    
    return $addon_settings;
}