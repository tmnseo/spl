function fn_settings_variants_addons_cp_advanced_password_pass_additional_settings()
{
    $settings_scheme = fn_get_schema('cp_advanced_password', 'password_settings');
    $settings = array();
    
    if (!empty($settings_scheme)) {
        foreach ($settings_scheme as $setting_key => $setting) {
            $settings[$setting_key] = $setting['name'];
        }
    }

    return $settings;
}